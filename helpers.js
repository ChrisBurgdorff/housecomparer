function getPayment(saleAmount, downPayment, interestRate, zipCode) {
	//TODO: replace sale amount with tax assessment from zillow
    var loanAmount = saleAmount - downPayment;
    var term = 360;
    var monthlyInterest = interestRate / 1200;
    var principalAndInterest = loanAmount * (monthlyInterest * Math.pow((1 + monthlyInterest), term))/(Math.pow((1 + monthlyInterest), term) - 1);
    var includeInsurance = principalAndInterest + 67.0;
    var includeTaxes = includeInsurance + ( ( (getTaxRate(zipCode) / 100) * saleAmount) / 12);
    if ( (downPayment / saleAmount) < 0.2) {
        //Include PMI
        var pmi = (0.0059 * loanAmount) / 12;
        includeTaxes = includeTaxes + pmi;
    }
    return includeTaxes;
}

$('.money').keyup(function(event) {
    // skip for arrow keys
    if(event.which >= 37 && event.which <= 40) return;
    // format number
    $(this).val(function(index, value) {
        return value
        .replace(/\D/g, "")
        .replace(/\B(?=(\d{3})+(?!\d))/g, ",");
    });
});

